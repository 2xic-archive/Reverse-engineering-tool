




import struct
def hex_2_string(input_stirng):
	for string in input_stirng.split("\t"):
		hex_int = int(string, 16)
		#	endianness <3 
		bytes_string = struct.pack('<I', hex_int)
		results = []
		for byte in bytes_string:
			results.append(chr(byte))
			if(byte == 0):
				results.append("\n")
		print("".join(results), end="")

